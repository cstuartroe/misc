public class Sentence
{
    private String english;
    private String lauvinko;
    
    public Sentence(String _english, String _lauvinko)
    {
        english = _english;
        lauvinko = _lauvinko;
    }
    
    public String getEnglish()
    {
        return english;
    }
    
    public String getLauvinko()
    {
        return lauvinko;
    }
}